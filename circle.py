import math


def area(r):
    '''Принимает на вход одно число - радиус окружности. Возвращает единственное число - площадь окружности с заданным радиусом'''
    return math.pi * r * r


def perimeter(r):
    '''Принимает на вход одно число - радиус окружности. Возвращает единственное число - периметр окружности с заданным радиусом'''
    return 2 * math.pi * r


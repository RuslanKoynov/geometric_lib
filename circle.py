import math


def area(r):
    '''
    Return the circle area.

        arguments:
            r (auto): circle radius.
        
        return value:
            math.pi * r * r (auto): π multiply by circle radius.
    '''

    return math.pi * r * r


def perimeter(r):
    '''
    Return the circle perimeter.

        arguments:
            r (auto): circle radius.
        
        return value:
            2 * math.pi * r (auto): 2 multiply by π multiply by circle radius.
    '''

    return 2 * math.pi * r


import math


def area(r):
    
    '''
    принимает значение радиуса окружности, возвращает значение площади круга
    '''
    return math.pi * r * r

def perimeter(r):
        
    '''
    принимает значение радиуса окружности, возвращает значение длины окружности
    '''
    return 2 * math.pi * r
    


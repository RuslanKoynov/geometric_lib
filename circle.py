import math
'''Принимает число r и возвращает площадь круга'''
def area(r):
    return math.pi * r * r
'''Пример вызова:
    result = area(5)
    print(result) # Выведет 78.5'''
    
'''Приннимает число r и возвращает периметр круга'''
def perimeter(r):
    return 2 * math.pi * r
'''Пример вызова:
    result = perimetr(5)
    print(result) # Выведет 31.4'''
    
    

import math


def area(r):
    return math.pi * r * r
''' Принимает число r, возвращает произведение числа Пи и квадрата r
Возвращаемое значение: площадь круга'''

def perimeter(r):
    return 2 * math.pi * r
''' Принимает число r, возвращает произведение 2, числа Пи, r
Возвращаемое значение: периметр круга'''


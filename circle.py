import math


def area(r):
    """
    Принимает радиус круга, возвращает его площадь
    Входные данные: 7
    Выходные данные: 78.53981633974483
    """
    return math.pi * r * r


def perimeter(r):
    """
        Принимает радиус окружности, возвращает ее длину
        Входные данные: 5
        Выходные данные: 31.41592653589793
    """
    return 2 * math.pi * r

import math
def area(r):
    '''Принимает число r, возвращает произведение числа пи на число r в квадрате
    :param число r
    :return число, которое является произведением числа r в квадрате и числа пи '''
    return math.pi * r * r
def perimeter(r):
    '''Принимает число r, возвращает произведение числа 2,числа пи, числа r
    :param число r
    :return число, которое является произведением числа 2, числа пи и числа r '''
    return 2 * math.pi * r

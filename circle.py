import math


def area(r):
    '''
        Принимает: десятичное число r - радиус окружности.
        Возвращает: число r в квадрате, умноженное на число Пи - площадь окружности.
    '''
    return math.pi * r * r


def perimeter(r):
    '''
        Принимает: десятичное число r - радиус окружности.
        Возвращает: число r умноженное на 2, умноженное на число Пи - длина окружности.
    '''
    return 2 * math.pi * r


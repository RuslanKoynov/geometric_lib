import math


def area(r):
    '''
     функция принимет радиус круга
     функция возвращает умножение числа пи на квадрат радиуса
    '''
    return math.pi * r * r


def perimeter(r):
    '''
     функция принимет радиус круга
     функция возвращает умножение числа пи на два радиуса
    '''
    return 2 * math.pi * r


import math
def area(r):
    '''One number (r) is taken as input:
        r (float) is the radius of a circle.'''
    '''Returns the area of a circle with an input value.'''
    return math.pi * r * r
def perimeter(r):
    '''One number (r) is taken as input:
        r (float) is the radius of a circle.'''
    '''Returns the peremiter of a circle with an input value.'''
    return 2 * math.pi * r


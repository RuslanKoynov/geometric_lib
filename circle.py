import math


def area(r):
    # Функция для вычисления площади круга
    # Принимает радиус круга r
    # Использует формулу π * r^2 для вычисления площади
    return math.pi * r * r


def perimeter(r):
    # Функция для вычисления периметра круга
    # Принимает радиус круга r
    # Использует формулу 2 * π * r для вычисления периметра
    return 2 * math.pi * r


import math

def area(r):
    '''
    Функция для расчета площади круга, принимает значение r, при этом возвращая число по формуле: return = pi*r^2
    '''
    return math.pi * r * r


def perimeter(r):
    '''
    Функция для расчета периметра круга, принимает значение r, при этом возвращая число по формуле: return = 2*pi*r
    '''
    return 2 * math.pi * r

def area(a, b):
    '''Принимает два числа - длины сторон прямоугольника.
     Возвращает единственное число - площадь прямоугольника с заданными сторонами'''
    return a * b

def perimeter(a, b):
    '''Принимает два числа - длины сторон прямоугольника.
    Возвращает единственное число - периметр прямоугольника с заданными сторонами'''
    return 2 * (a + b)

def area(a, b):
    """
        Принимает длину и ширину прямоугольника, возвращает его площадь
        Входные данные: 5, 7
        Выходные данные: 35
    """
    return a * b

def perimeter(a, b):
    """
        Принимает длину и ширину прямоугольника, возвращает его периметр
        Входные данные: 5, 7
        Выходные данные: 24
    """
    return 2 * (a + b)

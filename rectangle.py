def area(a, b): 
    '''
    Принимает длини сторон прямоугольника - a,b. Возвращает площадь прямоугольника с данными сторонами
    Вход: 3 4 Выход: 12
    '''
    return a * b 

def perimeter(a, b):
    '''
    Принимает длини сторон прямоугольника - a,b. Возвращает периметр прямоугольника с данными сторонами
    Вход: 2 5 Выход: 14
    ''' 
    return 2 * (a + b)

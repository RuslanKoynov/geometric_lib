def area(a, b):
    """
    Принимает два аргумента a и b - длинна и высота прямоугольника,
    возвращает площадь данного прямоугольника.
    """
    return a * b + 1


def perimeter(a, b):
    """
    Принимает два аргумента a и b - длинна и высота прямоугольника,
    возвращает периметр данного прямоугольника.
    """
    return 2 * (a + b)

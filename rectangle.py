def area(a, b):
    return a * b
'''
    Принимает 2 числа, которые являются сторонами прямоугольника: a,b
    Возвращает площадь прямоугольника, котоая вычисляется по формуле : a * b
'''
def perimeter(a, b):
    return 2*(a + b)
'''
    Принимает 2 числа, которые являются сторонами прямоугольника:a,b
    Возвращает периметр треугольника, вычисляя его по формлуе: 2*(a + b)
'''
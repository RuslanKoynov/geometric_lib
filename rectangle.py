def area(a, b):

    '''
    Принимает: десятичные числа a и b - длина и ширина соответственно.
    Возвращает: произведение a и b - площадь прямоугольника соответственно.
    '''
    return a * b

def perimeter(a, b):

    '''
    Принимает: десятичные числа a и b - длина и ширина соответственно.
    Возвращает: сумму удвоенной стороны a и удвоенной стороны b.
    '''

    return ((2*a) + (2*b))
def area(a, b):
    """ Returns a rectangle area

    Args:
        a (integer/double): first side of the rectangle
        b (integer/double): second side of the rectangle

    Returns:
        (integer/double): area of the rectangle
    """
    return a * b 


def perimeter(a, b):
    """Returns a perimeter of the rectangle

    Args:
        a (integer/double): first side of the rectangle
        b (integer/double): second side of the rectangle

    Returns:
        (integer/double): perimeter of the rectangle
    """
    return 2 * (a + b)

def area(a, b):
    '''
    Принимает два числа, которые являются сторонами прямоугольника: a, b
    Возвращает площадь прямоугольника, вычисляя ее по формуле: a * b
    '''
    return a * b


def perimeter(a, b):
    '''
    Принимает два числа, которые являются сторонами прямоугольника: a, b
    Возвращает периметр прямоугольника, вычисляя его по формуле: a * b
    '''
    return (a + b) * 2

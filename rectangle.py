def area(a, b): 
    '''
    Возвращает площадь прямоугольника

      Параметры:
           a (int):значение одной стороны прямоугольника
           b (int):значение второй стороны прямоугольника
    ''' 
    return a * b 

def perimeter(a, b): 
    '''
    Возвращает периметр прямоугольника

      Параметры:
            a (int):значение одной стороны прямоугольника
           b (int):значение второй стороны прямоугольника
    '''
    return 2*(a + b)

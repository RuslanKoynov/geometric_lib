def area(a, b):
    '''
    Принимает числа, которые являются сторонами прямоугольника
    Возвращает площадь прямоугольника, вычисляя ее по формуле: S=ab
    '''
    return a * b
def perimeter(a, b):
    '''
    Принимает числа, которые являются сторонами прямоугольника
    Возвращает периметр прямоугольника, вычисляя ее по формуле: P=2a+2b
    '''
    return 2*a + 2*b



def area(a, b):

    '''
    Принимает: десятичные числа a и b - длина и ширина.
    Возвращает: произведение a и b - площадь прямоугольника.
    '''
    return a * b

def perimeter(a, b):

    '''
    Принимает: десятичные числа a и b - длина и ширина.
    Возвращает: сумму a и b умноженную на 2 - периметр прямоугольника.
    '''

    return 2 * (a + b)




def area(a, b):
    return a*b
'''Принимает числа a и b, возвращает произведение a и b
Возвращаемое значение: площадь прямоугольника'''
def perimeter(a, b):
    return 2 * (a + b)
'''Принимает числа a и b, возвращает произведение 2 и суммы a и b
Возвращаемое значение: периметр прямоугольника'''

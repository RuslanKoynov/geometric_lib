def area(a, b):
    '''Принимает числа a,b, возвращает произведение чисел a и b
    :param числа a и b
    :return произведение чисел a и b'''
    return a * b
def perimeter(a, b):
    '''Принимает числа a и b, возвращает сумму произведений чисел 2 на a и чисел 2 на b
    :param числа a и b
    :return сумма произведений чисел 2 и a и чисел 2 и b'''
    return 2*a + 2*b
def area(a, b):
    """
    Функция принимает 2 числа - a и b (стороны прямоугольника)
    Далее возвращает произведение a и b (т.е. площадь прямоугольника)
    """
    return a * b

def perimeter(a, b):
    """
    Функция принимает 2 числа - a и b (стороны прямоугольника)
    Далее возвращает удвоенную сумму a и b (т.е. периметр прямоугольника)
    """
    return 2*a + 2*b
def area(a, b):

    '''Принимает числа a и b - стороны прямоугольника.
    Возращает произведение a и b, т.е. площадь прямоугольника.'''

    return a * b 
    
def perimeter(a, b): 

    '''Принимает числа a и b - стороны прямоугольника.
    Возращет удвоенную сумму a и b, т.е. пириметр прямоугльника.'''
    
    return 2 * (a + b) 
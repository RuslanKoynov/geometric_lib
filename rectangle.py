def area(a, b): 
    # Функция для вычисления площади прямоугольника
    # Принимает длину a и ширину b прямоугольника
    # Вычисляет площадь прямоугольника по формуле a * b
    return a * b 

def perimeter(a, b):
    # Функция для вычисления периметра прямоугольника
    # Принимает длину a и ширину b прямоугольника
    # Вычисляет периметр прямоугольника по формуле (a + b) * 2
    return (a + b)*2

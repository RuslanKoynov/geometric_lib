def area(a, b):
    '''Two numbers (a, b) are taken as input:
        a, b (all nums are float) are the side of the rectangle.'''
    '''Returns the area of the square with an input value.'''
    return a * b
def perimeter(a, b):
    '''Two numbers (a, b) are taken as input:
        a, b (all nums are float) are the side of the rectangle.'''
    '''Returns the perimeter of the square with an input value.'''
    return 2 * (a + b)

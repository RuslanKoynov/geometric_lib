def area(a, b):
    #Функция принимает две переменные - стороны прямоугольника.
    return a * b
    #функция возвращает площадь прямоугольника.

def perimeter(a, b):
    #функция принимает две переменные - стороны прямоугольника.
    return 2*(a + b)
    #функция возвращает периметр прямоугольника.

print(perimeter(9,3))
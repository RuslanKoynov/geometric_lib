def area(a, b):
    '''The area() function calculates the area of a rectangle using the formula l * w. Takes length and width of rectangle.'''
    return a * b


def perimeter(a, b):
    '''The perimeter() function calculates the perimeter of a rectangle using the formula 2(l + w). Takes lenght nd width of rectangle.'''
    return 2 * (a + b)

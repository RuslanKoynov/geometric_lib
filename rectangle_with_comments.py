def area(a, b): 
    # Функция для нахождения площади прямоугольника
    # Принимает на вход два аргумента: стороны прямоугольника
    # Возвращает произведение двух сторон - площадь прямоугольника
    return a * b 

def perimeter(a, b): 
    # Функция для нахождения площади прямоугольника
    # Принимает на вход два аргумента: стороны прямоугольника
    # Возвращает удвоенную сумму двух сторон - периметр прямоугольника
    return (a + b) * 2

def area(a):
    '''
    Return the square area.

        arguments:
            a (auto): side of a square.

        return value:
            a * a (auto): product of 2 side of the square.
    '''
    return a * a


def perimeter(a):
    '''
    Return the square perimeter.

        arguments:
            a (auto): side of a square.
        
        return value:
             4 * a (auto): quadruple the side of a square.
            
    '''
    return 4 * a

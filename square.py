
def area(a):
    """
    Функция принимает число a (сторону квадрата)
    Далее возвращает число a возведенное в квадрат (т.е. площадь квадрата)
    """
    return a * a


def perimeter(a):
    """
    Функция принимает число a (сторону квадрата)
    Далее возвращает число a умноженное на 4 (т.е. периметр квадрата)
    """
    return 4 * a


def area(a):
    '''
    Возвращает площадь квадрата.

        Параметры:
            a(int): сторона квадрата
        
        Возвращенное значение:
            a * a (int): площадь квадрата 
    '''
    return a * a


def perimeter(a):
    '''
    Возвращает периметр квадрата.

        Параметры:
            a(int): сторона квадрата
        
        Возвращенное значение:
            4 * a (int): площадь квадрата 
    '''
    return 4 * a


def area(a):
    # Функция для вычисления площади квадрата
    # Принимает длину стороны a квадрата
    # Вычисляет площадь квадрата по формуле a * a
    return a * a


def perimeter(a):
    # Функция для вычисления периметра квадрата
    # Принимает длину стороны a квадрата
    # Вычисляет периметр квадрата по формуле 4 * a
    return 4 * a


def area(a):
    '''
    возвращает площадь квадрата, которая вычисляется как а*а

    параметры: 
    а: сторона квадрата

    возвращаемое значение:
    a*a: площадь квадрата
    '''
    return a * a


def perimeter(a):
    '''
    возвращает периметр квадрата, который вычисляется как произведение стороны а на 4
    
    параметры:
    а: сторона квадрата
    
    возвращаемое значение:
    4 * а: периметр квадрата
    '''
    return 4 * a

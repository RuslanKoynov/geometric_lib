def area(a):
    '''
    Функция принимает один параметр: a - длина стороны квадрата.
    Возвращает значение площади квадрата, вычисленной по формуле a * a.
    '''

    return a * a


def perimeter(a):
    '''
    Функция принимает один параметр: a - длина стороны квадрата.
    Возвращает значение периметра квадрата, вычисленное как 4 * a.
    '''

    return 4 * a

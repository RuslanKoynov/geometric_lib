
def area(a):
    ''' 
    Принимает число a, возвращает квадрат числа a
        Параметры: 
            a (int): десятичное число
        Возвращаемое значение:
            area (int): квадрат числа a
    '''
    return a * a


def perimeter(a):
    '''
    Принимает число a, возращает умножение числа на 4
        Параметры:
            a (int): десятичное число
        Возвращаемое значение:
            perimeter (int): результат умножения числа на 4
    '''            
    return 4 * a

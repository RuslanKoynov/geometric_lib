
def area(a):
    '''
    Возвращает квадрат входного числа.
    :param a: десятичное число
    :return: число, состоящее из квадрата входного десятичного числа
    '''
    return a * a


def perimeter(a):
    '''
    Возвращает число, увеличенное в 4 раза.
    :param a: десятичное число
    :return: число, состоящее из входного десятичного числа, увеличенного в 4 раза
    '''
    return 4 * a

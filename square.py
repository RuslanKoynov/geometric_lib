def area(a):
    '''
    Принимает число, которое является стороной квадрата
    Возвращает площадь квадрата, вычисляя ее по формуле: S=a*a
    '''
    return a * a

def perimeter(a):
    '''
    Принимает число, которое является стороной квадрата
    Возвращает периметр квадрата, вычисляя его по формуле: P=4*a
    '''
    return 4 * a

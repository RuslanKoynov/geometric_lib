def area(a):
    """
    Возвращает площадь квадрата.

    Параметры:
    a (int, float): сторона квадрата.

    Возвращаемое значение:
    float: площадь квадрата.
    """
    return a * a

def perimeter(a):
    """
    Возвращает периметр квадрата.

    Параметры:
    a (int, float): сторона квадрата.

    Возвращаемое значение:
    float: периметр квадрата.
    """
    return 4 * a

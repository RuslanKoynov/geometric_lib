def area(a):
    """
    Функция, которая вычисляет площадь квадрата по заданной стороне.

    Args:
        a (float): Сторона квадрата.

    Returns:
        float: Площадь квадрата.
    """
    return a * a

def perimeter(a):
    """
    Функция, которая вычисляет периметр квадрата по заданной стороне.

    Args:
        a (float): Сторона квадрата.

    Returns:
        float: Периметр квадрата.
    """
    return 4 * a

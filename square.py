def area(a):
    '''
    Возвращает квадрат числа.
    :param a: десятичное число.
    :return: число, состоящее из произведения a на самого себя.
    '''
    return a * a


def perimeter(a):
    '''
    Возвращает число a, увеличенное в четыре раза.
    :param a: десятичное число.
    :return: число, состоящее из произведения a на 4.
    '''
    return 4 * a

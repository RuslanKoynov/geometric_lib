def area(a):
    '''
    Принимает число, которое является стороной квадрата: a
    Возвращает площадь квадрата, вычисляя ее по формуле: a ^ 2
    '''
    return a * a


def perimeter(a):
    '''
    Принимает число, которое является стороной квадрата: a
    Возвращает периметр квадрата, вычисляя его по формуле: 4 * a
    '''
    return 4 * a

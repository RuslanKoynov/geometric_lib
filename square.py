def area(a):
    """Returns a square area

    Args:
        a (integer/double): side of the square

    Returns:
        (integer/double): area of the square
    """
    return a * a


def perimeter(a):
    """Returns a perimeter of the square

    Args:
        a (integer/double): side of the square

    Returns:
        (integer/double): perimeter of the square
    """
    return 4 * a

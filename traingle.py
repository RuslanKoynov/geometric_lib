def area(a, h):
    '''
    Принимает числа, которые являются стороной и высотой треугольника
    Возвращает площадь треугольника, вычисляя ее по формуле: S=ah/2
    '''
    return a * h / 2
def perimeter(a, b, c):
    '''
    Принимает числа, которые являются стороной и высотой треугольника
    Возвращает периметр треугольника, вычисляя его по формуле: S=a+b+c
    '''
    return a + b + c

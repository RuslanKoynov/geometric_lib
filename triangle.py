def area(a, h):
    '''Two numbers (a, h) are taken as input:
        a (float) is the side of the triangle,
        h (float) is the height of the triangle.'''
    '''Returns the area of the triangle with an input value.'''
    return a * h / 2
def perimeter(a, b, c):
    '''Three numbers (a, b, c) are taken as input:
        a, b, c (all nums are float) are the side of the triangle.'''
    '''Returns the perimeter of the triangle with an input value.'''
    return a + b + c

def area(a, h): 
    '''
    Функция принимает два параметра: a - длина основания треугольника, h - высота треугольника.
    Возвращает значение площади треугольника, вычисленной по формуле a * h / 2.
    '''
    return a * h / 2 

def perimeter(a, b, c): 
    '''
    Функция принимает три параметра: a, b, c - длины сторон треугольника.
    Возвращает значение периметра треугольника, вычисленное как сумма всех сторон a + b + c.
    '''
    return a + b + c 
def area(a, h):
    """ 
    Возвращает площадь треугольника.
        
        Параметры:
            a - основание треугольника
            h - высота треугольника
    """ 
    return a * h / 2 

def perimeter(a, b, c):
    """ Возврашает периметр треугольника, со сторонами a, b, c """
    return a + b + c 

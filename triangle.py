def area(a, h):
    '''Принимает на вход два числа - длину стороны (a) и длину высоты (h) треугольника. Возвращает единственное число - площадь треугольника по
    заданным параметрам'''
    return a * h / 2

def perimeter(a, b, c):
    '''Принимает на вход три числа - длины сторон треугольника.
    Возвращает единственное число - периметр треугольника с заданными сторонами'''
    return a + b + c

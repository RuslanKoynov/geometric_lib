def area(a, h):
    """Returns a triangle area

    Args:
        a (integer/double): side of the triangle
        h (integer/double): hight of the triangle

    Returns:
        (integer/double): area of the triangle
    """
    return a * h / 2 


def perimeter(a, b, c):
    """Returns a perimeter of the triangle

    Args:
        a (integer/double): first side of the triangle
        b (integer/double): second side of the triangle
        c (integer/double): third side of the triangle

    Returns:
        (integer/double): perimeter of the triangle
    """
    return a + b + c

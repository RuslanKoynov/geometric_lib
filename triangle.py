def area(a, h): 
    # Функция для вычисления площади треугольника
    # Принимает основание a и высоту h треугольника
    # Вычисляет площадь треугольника по формуле (a * h) / 2
    return a * h / 2 

def perimeter(a, b, c): 
    # Функция для вычисления периметра треугольника
    # Принимает длины сторон a, b, c треугольника
    # Вычисляет периметр треугольника по формуле a + b + c
    return a + b + c

def area(a, h):
    '''
    принимает значение длин основания и высоты треугольника, возвращает значение площади треугольника 
    '''
    return a * h / 2

def perimeter(a, b, c):
    '''
    принимает значения длин сторон треугольника, возвращает значение периметра треугольника
    '''
    return a + b + c
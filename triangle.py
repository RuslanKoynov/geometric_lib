def area(a, h):
    '''The area() function computes the area of a triangle using the formula (base * height) / 2. Takes base and height of a triangele.'''
    return a * h / 2


def perimeter(a, b, c):
    '''The perimeter() function calculates the perimeter of a triangle using the formula a + b + c, where a, b, and c represent the lengths of the triangle's three sides. Takes three lengths of triangle.'''
    return a + b + c

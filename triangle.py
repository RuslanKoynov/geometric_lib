def area(a, h): 

    """
    This function calculates the area of a triangle based on its base length 'a' and height 'h'.

    Formula:
    Area = 0.5 * a * h
    
    Parameters:
    - a (float): The length of the base of the triangle.
    - h (float): The height of the triangle.
    
    Return value:
    - area (float): The area of the triangle.
    """

    return a * h / 2 

def perimeter(a, b, c): 

    """
    This function calculates the perimeter of a triangle based on its side lengths 'a', 'b', and 'c'.

    Formula:
    Perimeter = a + b + c
    
    Parameters:
    - a (float): The length of one side of the triangle.
    - b (float): The length of another side of the triangle.
    - c (float): The length of the third side of the triangle.
    
    Return value:
    - perimeter (float): The perimeter of the triangle.
    """

    return a + b + c 

def area(a, h):
    '''Принимает числа a,h, возвращает частное произведения чисел (a и h) и 2
    :param числа a и h
    :return частное произведения чисел a и h и числа 2'''
    return a * h / 2 
def perimeter(a, b, c):
    '''Принимает числа a,b,c, возвращает сумму чисел a,b,c
    :param числа a,b,c
    :return сумма чисел a,b и c'''
    return a + b + c
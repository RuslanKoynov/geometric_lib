def area(a, h):
    '''
    Функция рассчитывает площадь треугольника, получая на вход a,h и возвращая число по формуле a*h/2
    '''
    return a * h / 2 

def perimeter(a, b, c):
    '''
    Функция рассчитывает периметр треугольника, получая на вход a,h,c и возвращая число по формуле a+b+c
    '''
    return a + b + c 

def area(a, h):
    '''
    a: сторона треугольника
    h: высота треугольника
    Возвращаемое значение: произведение a и h деленное на два
    '''
    return a * h / 2 

def perimeter(a, b, c):
    '''
    a: сторона треугольника
    b: сторона треугольника
    c: сторона треугольника
    Возвращаемое значение: сумма трех чисел a,b и с
    '''
    return a + b + c


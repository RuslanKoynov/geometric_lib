def area(a, h):
    #функция принимает две переменные a и h - сторону треугольника и высоту.
    return a * h / 2 
    #функция возвращает площадь треугольника.
    
def perimeter(a, b, c):
    #Функция принимает три переменные a, b, c - стороны треугольника.
    return a + b + c
    #Функция возвращает периметр треугольника.


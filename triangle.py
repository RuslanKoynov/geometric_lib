def area(a, h):
    """
    Принимает два параметра a и h - ребро и высоту треугольника,
    возвращает площадь данного треугольника.
    """
    return a * h / 2


def perimeter(a, b, c):
    """
    Принимает три параметра a, b и c - рёбра треугольника,
    возвращает периметр данного треугольника.
    """
    return a + b + c

def area(a, h): 
    # Функция для нахождения площади треугольника
    # Принимает на вход два аргумента: сторону треугольника и проведенную к ней высоту
    # Возвращает половину произведения стороны и высоты - площадь треугольника
    return a * h / 2 

def perimeter(a, b, c): 
    # Функция для нахождения периметра треугольника
    # Принимает на вход три аргумента - стороны треугольника
    # Возвращает сумму сторон - периметр треугольника
    return a + b + c 